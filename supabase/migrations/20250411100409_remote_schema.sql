CREATE OR REPLACE FUNCTION "public"."match_training_sources"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "p_chatbot_id" "uuid") RETURNS TABLE("id" "uuid", "content" "text", "source_type" "text", "url" "text", "file_path" "text", "status" "text", "chatbot_id" "uuid", "created_at" timestamp with time zone, "size_kb" numeric, "embedding" "public"."vector", "similarity" double precision, "title" "text")
    LANGUAGE "plpgsql"
    AS $_$
BEGIN
    RETURN QUERY
    SELECT
        ts.id,
        ts.content,
        ts.source_type,
        ts.url,
        ts.file_path,
        ts.status,
        ts.chatbot_id,
        ts.created_at,
        ts.size_kb,
        ts.embedding,
        1 - (ts.embedding <=> query_embedding) as similarity,
        -- Extract a meaningful title from file_path or url
        CASE 
            WHEN ts.file_path IS NOT NULL AND ts.file_path != '' THEN 
                CASE 
                    WHEN ts.file_path LIKE '%.pdf' THEN 
                        regexp_replace(substring(ts.file_path from '[^/\\]+\.pdf$'), '\.pdf$', '', 'i')
                    ELSE 
                        substring(ts.file_path from '[^/\\]+$')
                END
            WHEN ts.url IS NOT NULL AND ts.url != '' THEN 
                CASE
                    WHEN position('/' in ts.url) > 0 THEN
                        substring(ts.url from '^(?:https?://)?(?:www\.)?([^/]+)')
                    ELSE
                        ts.url
                END
            ELSE 
                'Source ' || substring(ts.id::text, 1, 8)
        END as title
    FROM
        training_sources ts
    WHERE
        ts.chatbot_id = p_chatbot_id
        AND ts.status = 'trained'
        AND ts.embedding IS NOT NULL
        AND 1 - (ts.embedding <=> query_embedding) > match_threshold
    ORDER BY
        -- Order purely by similarity score
        ts.embedding <=> query_embedding
    LIMIT match_count;
END;
$_$;


