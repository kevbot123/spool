CREATE OR REPLACE FUNCTION "public"."user_owns_chat_log"("log_id" "uuid") RETURNS boolean
    LANGUAGE "sql" SECURITY DEFINER
    SET "search_path" TO 'public'
    AS $$
  select exists (
    select 1
    from public.chat_logs cl
    join public.chatbots cb on cl.chatbot_id = cb.id
    where cl.id = log_id
    and cb.user_id = auth.uid() -- Compare log's owner with the current user
  );
$$;


